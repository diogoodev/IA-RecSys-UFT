
# %%
a = 10
b = 20
print("a = " + str(a)+"; b = " + str(b))

# Operador ==
if a == b:
    print("a é igual a b")
else:
    print("a é diferente de b")

# Operador !=

if a != b:
    print("a é diferente de b")
else:
    print("a é igual a b")

# Operador >

if a > b:
    print("a é maior do que b")
else:
    print("a é menor ou igual a b")

# Operador <

if a < b:
    print("a é menor do que b")
else:
    print("a é maior ou igual a b")


# Operador and
if a < 100 and b < 100:
    print("a e b são menores que 100")
else:
    print("a e b são maiores que 100")

# Operador or
if a < 100 or b < 100:
    print("a ou b são menores que 100")
else:
    print("a e b são maiores ou iguais a 100")

# Operador not
if not (a > b):
    print("a não é maior que b")
else:
    print("a é maior que b")
