# Inteiros (int) - usado para números inteiros

# %%
num = 10
print(num)
print(type(num))  # Saida: <class 'int'>

# Números de ponto flutuante (float)

# %%
pi = 3.14
print(pi)
print(type(pi))  # Saida: <class 'float'>)

# Strings (str) - usados para texto

# %%
greeting = "Hello, world"
print(greeting)
print(type(greeting))  # Saida: <class 'str'>

# Booleanos (bool) - Usados

# %%
is_python_fun = True
print(is_python_fun)
print(type(is_python_fun))  # Saida: <class 'bool'>


# Listas (list) - usadas para coleções ordenadas de valores

# %%
numbers = [1, 2, 3, 4, 5]
print(numbers)
print(type(numbers))

# Tuplas (tuple) - semelhante às listas, mas imutáveis

# %%
coordinates = (10.0, 20.0)
print(coordinates)
print(type(coordinates))  # Saida: <class 'tuple'>

# Dicionários (dict) - usados para coleções de pares
# %%
person = {"name": "Alice",  "age": 25}
print(person)
print(type(person))
